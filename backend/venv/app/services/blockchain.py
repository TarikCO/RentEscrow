from web3 import Web3
import json
from app.core.config import get_settings
import os
from decimal import Decimal

settings = get_settings()

# Hardhat's default local network address
HARDHAT_URL = settings.hardhat_url

class BlockchainService:
    def __init__(self):
        # 1. Connect to the local Hardhat node
        self.w3 = Web3(Web3.HTTPProvider(HARDHAT_URL))
        
        # 2. Load the contract address from .env
        self.contract_address = settings.rent_escrow_address

        # Load landlord address from .env
        self.landlord_address = settings.landlord_address
        
        # 3. Load ABI/bytecode from Hardhat artifacts
        self.artifact = self._load_artifact()
        self.abi = self.artifact.get("abi") if self.artifact else None
        self.bytecode = self.artifact.get("bytecode") if self.artifact else None
        
        # 4. Initialize the contract object
        if self.abi and self.contract_address != "0xe7f1725E7734CE288F8367e1Bb143E90bb3F0512":
            self.contract = self.w3.eth.contract(
                address=self.w3.to_checksum_address(self.contract_address), 
                abi=self.abi
            )
        else:
            self.contract = None

    def is_contract_loaded(self):
        return self.contract is not None

    def _load_artifact(self):
        """Finds the ABI/bytecode JSON generated by Hardhat."""
        try:
            abi_path = os.path.join(
                os.path.dirname(__file__), 
                "../../../../blockchain/artifacts/contracts/RentEscrow.sol/RentEscrow.json"
            )
            with open(abi_path, "r") as f:
                return json.load(f)
        except FileNotFoundError:
            print("⚠️ Warning: Escrow.json ABI not found. Run 'npx hardhat compile' first.")
            return None

    def _actor_private_key(self, actor: str | None):
        if actor == "landlord":
            return settings.landlord_private_key or "0x59c6995e998f97a5a0044966f0945389dc9e86dae88c7a8412f4603b6b78690d"
        return settings.private_key

    def _sign_and_send(self, tx: dict, private_key: str):
        account = self.w3.eth.account.from_key(private_key)
        tx.setdefault("nonce", self.w3.eth.get_transaction_count(account.address))
        tx.setdefault("chainId", self.w3.eth.chain_id)
        tx.setdefault("from", account.address)

        latest_block = self.w3.eth.get_block("latest")
        base_fee = latest_block.get("baseFeePerGas", 0)
        priority_fee = self.w3.to_wei(1, "gwei")
        tx.setdefault("maxPriorityFeePerGas", priority_fee)
        tx.setdefault("maxFeePerGas", int(base_fee * 2 + priority_fee))

        if "gas" not in tx:
            tx["gas"] = self.w3.eth.estimate_gas(tx)

        signed = self.w3.eth.account.sign_transaction(tx, private_key)
        tx_hash = self.w3.eth.send_raw_transaction(signed.raw_transaction)
        receipt = self.w3.eth.wait_for_transaction_receipt(tx_hash)
        return tx_hash.hex(), receipt

    def _persist_contract_address(self, contract_address: str):
        env_path = os.path.join(os.path.dirname(__file__), "../../../../.env")
        if not os.path.exists(env_path):
            return

        with open(env_path, "r", encoding="utf-8") as file:
            lines = file.readlines()

        updated = []
        found = False
        for line in lines:
            if line.startswith("rent_escrow_address="):
                updated.append(f"rent_escrow_address={contract_address}\n")
                found = True
            else:
                updated.append(line)

        if not found:
            updated.append(f"rent_escrow_address={contract_address}\n")

        with open(env_path, "w", encoding="utf-8") as file:
            file.writelines(updated)

    def deploy_escrow(self, landlord: str, duration_days: int, yield_percent: int, rent_amount_eth: str):
        if not self.abi or not self.bytecode:
            raise RuntimeError("Contract artifact is missing ABI/bytecode.")

        landlord_address = self.w3.to_checksum_address(landlord)
        duration_seconds = int(duration_days) * 24 * 60 * 60
        rent_amount_wei = self.w3.to_wei(Decimal(str(rent_amount_eth)), "ether")

        account = self.w3.eth.account.from_key(settings.private_key)
        contract = self.w3.eth.contract(abi=self.abi, bytecode=self.bytecode)
        tx = contract.constructor(landlord_address, duration_seconds, int(yield_percent)).build_transaction(
            {
                "from": account.address,
                "value": rent_amount_wei,
            }
        )

        tx_hash, receipt = self._sign_and_send(tx, settings.private_key)
        deployed_address = receipt.contractAddress

        self.contract_address = deployed_address
        self.contract = self.w3.eth.contract(address=self.w3.to_checksum_address(deployed_address), abi=self.abi)
        self._persist_contract_address(deployed_address)

        return {
            "transaction_hash": tx_hash,
            "contract_address": deployed_address,
        }

    def confirm_lease(self, actor: str | None = None):
        if not self.contract:
            raise RuntimeError("Escrow contract is not loaded.")

        private_key = self._actor_private_key(actor)
        account = self.w3.eth.account.from_key(private_key)
        tx = self.contract.functions.confirmLease().build_transaction({"from": account.address})
        tx_hash, _ = self._sign_and_send(tx, private_key)
        return {"transaction_hash": tx_hash}

    def release_funds(self, actor: str | None = None):
        if not self.contract:
            raise RuntimeError("Escrow contract is not loaded.")

        private_key = self._actor_private_key(actor)
        account = self.w3.eth.account.from_key(private_key)
        tx = self.contract.functions.releaseFunds().build_transaction({"from": account.address})
        tx_hash, _ = self._sign_and_send(tx, private_key)
        return {"transaction_hash": tx_hash}

    def refund(self, actor: str | None = None):
        if not self.contract:
            raise RuntimeError("Escrow contract is not loaded.")

        private_key = self._actor_private_key(actor)
        account = self.w3.eth.account.from_key(private_key)
        tx = self.contract.functions.refund().build_transaction({"from": account.address})
        tx_hash, _ = self._sign_and_send(tx, private_key)
        return {"transaction_hash": tx_hash}

    def rate_landlord(self, score: int, actor: str | None = None):
        if not self.contract:
            raise RuntimeError("Escrow contract is not loaded.")

        private_key = self._actor_private_key(actor)
        account = self.w3.eth.account.from_key(private_key)
        tx = self.contract.functions.rateLandlord(int(score)).build_transaction({"from": account.address})
        tx_hash, _ = self._sign_and_send(tx, private_key)
        return {"transaction_hash": tx_hash}

    def check_escrow_balance(self):
        """Reads the current amount of ETH held by the contract"""
        if not self.contract:
            return 0
        balance_wei = self.w3.eth.get_balance(self.contract_address)
        return self.w3.from_wei(balance_wei, 'ether') # Convert to readable ETH

    def get_transaction_status(self):
        """Calls a public variable from your Escrow.sol"""
        if not self.contract:
            return "Contract Not Loaded"
        try:
            tenant_confirmed = self.contract.functions.tenantConfirmed().call()
            landlord_confirmed = self.contract.functions.landlordConfirmed().call()
            contract_balance = self.w3.eth.get_balance(self.contract_address)

            if contract_balance == 0:
                if tenant_confirmed and landlord_confirmed:
                    return "Funds Released"
                return "Refunded"

            if tenant_confirmed and landlord_confirmed:
                return "Confirmed by Both"
            if tenant_confirmed:
                return "Awaiting Landlord Confirmation"
            if landlord_confirmed:
                return "Awaiting Tenant Confirmation"
            return "Pending Confirmation"
        except Exception:
            # Backward compatibility with older single-confirmation deployments.
            is_confirmed = self.contract.functions.confirmed().call()
            return "Confirmed" if is_confirmed else "In Progress"
    
    def get_landlord_rating(self):
        if not self.contract:
            return 0.0
        
        try:
            # 1. Call the contract function
            # This returns the scaled integer (e.g., 450 for a 4.5 rating)
            scaled_rating = self.contract.functions.getAverageRating().call()
            
            # 2. Divide by 100 to get the actual decimal
            actual_rating = scaled_rating / 100.0
            
            return actual_rating
        except Exception as e:
            print(f"Error fetching rating: {e}")
            return 0.0

    def get_num_ratings(self):
        """Returns the number of ratings recorded on-chain."""
        if not self.contract:
            return 0

        try:
            return int(self.contract.functions.getNumRatings().call())
        except Exception as e:
            print(f"Error fetching number of ratings: {e}")
            return 0

    def get_escrow_overview(self):
        """Reads tenant, landlord and refund deadline directly from the escrow contract."""
        if not self.contract:
            return {
                "tenant": None,
                "landlord": None,
                "deadline": None,
                "rent_amount_eth": None,
                "yield_percent": None,
                "tenant_confirmed": None,
                "landlord_confirmed": None,
            }

        try:
            amount_wei = self.contract.functions.amount().call()
            return {
                "tenant": self.contract.functions.tenant().call(),
                "landlord": self.contract.functions.landlord().call(),
                "deadline": int(self.contract.functions.deadline().call()),
                "rent_amount_eth": float(self.w3.from_wei(amount_wei, "ether")),
                "yield_percent": int(self.contract.functions.yieldPercent().call()),
                "tenant_confirmed": bool(self.contract.functions.tenantConfirmed().call()),
                "landlord_confirmed": bool(self.contract.functions.landlordConfirmed().call()),
            }
        except Exception as e:
            print(f"Error fetching escrow overview: {e}")
            return {
                "tenant": None,
                "landlord": None,
                "deadline": None,
                "rent_amount_eth": None,
                "yield_percent": None,
                "tenant_confirmed": None,
                "landlord_confirmed": None,
            }
        
    def check_connection(self):
        return self.w3.is_connected() # Verify if node is live