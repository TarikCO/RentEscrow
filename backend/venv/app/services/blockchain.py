    def check_escrow_balance(self, escrow_address: str | None = None):
        contract, checksum = self._get_contract(escrow_address)
        if not contract or not checksum:
            return 0
        balance_wei = self.w3.eth.get_balance(checksum)
        return self.w3.from_wei(balance_wei, 'ether') # Convert to readable ETH

    def get_transaction_status(self, escrow_address: str | None = None):
        contract, checksum = self._get_contract(escrow_address)
        if not contract or not checksum:
            return "Contract Not Loaded"
        try:
            tenant_confirmed = contract.functions.tenantConfirmed().call()
            landlord_confirmed = contract.functions.landlordConfirmed().call()
            contract_balance = self.w3.eth.get_balance(checksum)

            if contract_balance == 0:
                if tenant_confirmed and landlord_confirmed:
                    return "Funds Released"
                return "Refunded"

            if tenant_confirmed and landlord_confirmed:
                return "Confirmed by Both"
            if tenant_confirmed:
                return "Awaiting Landlord Confirmation"
            if landlord_confirmed:
                return "Awaiting Tenant Confirmation"
            return "Pending Confirmation"
        except Exception:
            # Backward compatibility with older single-confirmation deployments.
            try:
                is_confirmed = contract.functions.confirmed().call()
                return "Confirmed" if is_confirmed else "In Progress"
            except Exception:
                return "Status Unavailable"
    
    def get_landlord_rating(self, escrow_address: str | None = None):
        contract, _ = self._get_contract(escrow_address)
        if not contract:
            return 0.0
        
        try:
            # 1. Call the contract function
            # This returns the scaled integer (e.g., 450 for a 4.5 rating)
            scaled_rating = contract.functions.getAverageRating().call()
            
            # 2. Divide by 100 to get the actual decimal
            actual_rating = scaled_rating / 100.0
            
            return actual_rating
        except Exception as e:
            print(f"Error fetching rating: {e}")
            return 0.0

    def get_num_ratings(self, escrow_address: str | None = None):
        """Returns the number of ratings recorded on-chain."""
        contract, _ = self._get_contract(escrow_address)
        if not contract:
            return 0

        try:
            return int(contract.functions.getNumRatings().call())
        except Exception as e:
            print(f"Error fetching number of ratings: {e}")
            return 0

    def get_escrow_overview(self, escrow_address: str | None = None):
        """Reads tenant, landlord and refund deadline directly from the escrow contract."""
        contract, checksum = self._get_contract(escrow_address)
        if not contract or not checksum:
            return {
                "address": escrow_address,
                "tenant": None,
                "landlord": None,
                "deadline": None,
                "rent_amount_eth": None,
                "yield_percent": None,
                "tenant_confirmed": None,
                "landlord_confirmed": None,
            }

        try:
            amount_wei = contract.functions.amount().call()
            tenant = contract.functions.tenant().call()
            landlord = contract.functions.landlord().call()
            deadline = int(contract.functions.deadline().call())
            yield_percent = int(contract.functions.yieldPercent().call())

            # Some older contract deployments may not expose tenantConfirmed/landlordConfirmed.
            # Keep core overview values available even when these optional reads fail.
            tenant_confirmed = None
            landlord_confirmed = None
            try:
                tenant_confirmed = bool(contract.functions.tenantConfirmed().call())
                landlord_confirmed = bool(contract.functions.landlordConfirmed().call())
            except Exception:
                try:
                    confirmed = bool(contract.functions.confirmed().call())
                    tenant_confirmed = confirmed
                    landlord_confirmed = confirmed
                except Exception:
                    tenant_confirmed = None
                    landlord_confirmed = None

            return {
                "address": checksum,
                "tenant": tenant,
                "landlord": landlord,
                "deadline": deadline,
                "rent_amount_eth": float(self.w3.from_wei(amount_wei, "ether")),
                "yield_percent": yield_percent,
                "tenant_confirmed": tenant_confirmed,
                "landlord_confirmed": landlord_confirmed,
            }
        except Exception as e:
            print(f"Error fetching escrow overview: {e}")
            return {
                "address": checksum,
                "tenant": None,
                "landlord": None,
                "deadline": None,
                "rent_amount_eth": None,
                "yield_percent": None,
                "tenant_confirmed": None,
                "landlord_confirmed": None,
            }

    def get_all_escrows(self):
        records = []
        for address in self.list_escrow_addresses():
            overview = self.get_escrow_overview(address)
            if not overview.get("tenant") or not overview.get("landlord"):
                continue

            records.append(
                {
                    **overview,
                    "transaction_status": self.get_transaction_status(address),
                    "average_rating": self.get_landlord_rating(address),
                    "num_ratings": self.get_num_ratings(address),
                    "escrow_balance_eth": float(self.check_escrow_balance(address)),
                }
            )

        return records
        
    def check_connection(self):
        return self.w3.is_connected() # Verify if node is live