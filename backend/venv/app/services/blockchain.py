    def rate_landlord(self, escrow_address: str, score: int, actor: str | None = None):
        contract, _ = self._get_contract(escrow_address)
        if not contract:
            raise RuntimeError("Escrow contract is not loaded.")

        private_key = self._actor_private_key(actor)
        account = self.w3.eth.account.from_key(private_key)
        tx = contract.functions.rateLandlord(int(score)).build_transaction({"from": account.address})
