from web3 import Web3
import json
from app.core.config import get_settings
import os

settings = get_settings()

# Hardhat's default local network address
HARDHAT_URL = settings.hardhat_url

class BlockchainService:
    def __init__(self):
        # 1. Connect to the local Hardhat node
        self.w3 = Web3(Web3.HTTPProvider(HARDHAT_URL))
        
        # 2. PLACEHOLDER: Contract Address
        # Replace this string after you run: npx hardhat ignition deploy ...
        self.contract_address = "0xe7f1725E7734CE288F8367e1Bb143E90bb3F0512"

        # landlord address
        self.landlord_address = "0x70997970C51812dc3A010C7d01b50e0d17dc79C8"
        
        # 3. Load the ABI from your Hardhat artifacts
        self.abi = self._load_abi()
        
        # 4. Initialize the contract object
        if self.abi and self.contract_address != "0xe7f1725E7734CE288F8367e1Bb143E90bb3F0512":
            self.contract = self.w3.eth.contract(
                address=self.w3.to_checksum_address(self.contract_address), 
                abi=self.abi
            )
        else:
            self.contract = None

    def _load_abi(self):
        """Finds the ABI JSON generated by Hardhat"""
        try:
            # Adjust this path to match your project structure
            abi_path = os.path.join(
                os.path.dirname(__file__), 
                "../../blockchain/artifacts/contracts/RentEscrow.sol/RentEscrow.json"
            )
            with open(abi_path, "r") as f:
                artifact = json.load(f)
                return artifact.get("abi") # Hardhat stores the ABI inside the JSON
        except FileNotFoundError:
            print("⚠️ Warning: Escrow.json ABI not found. Run 'npx hardhat compile' first.")
            return None

    def check_escrow_balance(self):
        """Reads the current amount of ETH held by the contract"""
        if not self.contract:
            return 0
        balance_wei = self.w3.eth.get_balance(self.contract_address)
        return self.w3.from_wei(balance_wei, 'ether') # Convert to readable ETH

    def get_transaction_status(self):
        """Calls a public variable from your Escrow.sol"""
        if not self.contract:
            return "Contract Not Loaded"
        # Accessing 'delivered' public variable from your screenshot
        is_delivered = self.contract.functions.delivered().call()
        return "Delivered" if is_delivered else "In Progress"
        
    def check_connection(self):
        return self.w3.is_connected() # Verify if node is live